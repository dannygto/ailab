// 动态导入函数（用于按需加载不常用图标）
export const loadIconDynamically = async (iconName: string) => {
  try {
    const iconModule = await import(`@mui/icons-material/${iconName}`);
    return iconModule.default;
  } catch (error) {
    console.warn(`Failed to load icon: ${iconName}`, error);
    return null;
  }
};

